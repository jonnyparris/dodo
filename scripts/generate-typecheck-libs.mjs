#!/usr/bin/env node
/**
 * Generate src/typecheck-libs.generated.ts — a static map of TypeScript's
 * default lib `.d.ts` files baked into the Worker bundle.
 *
 * The typecheck tool runs `tsc` inside a Durable Object, where there's no
 * filesystem to read lib files from. The TypeScript compiler resolves
 * `/// <reference lib="..." />` directives by calling `host.readFile()`
 * with paths like `lib.es2022.d.ts`, so we precompute the contents of every
 * `lib.*.d.ts` file in `node_modules/typescript/lib/` and ship them as
 * string literals.
 *
 * Run as part of `npm run build` so the generated file is fresh on every
 * deploy. The generated file is gitignored.
 *
 * Size note: the full lib set is ~3.2 MB raw, gzips to ~600 KB. Imported
 * lazily by typecheck.ts so non-typecheck cold starts pay nothing.
 */

import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
const libDir = join(repoRoot, "node_modules", "typescript", "lib");
const outFile = join(repoRoot, "src", "typecheck-libs.generated.ts");

const files = readdirSync(libDir).filter((f) => /^lib\..*\.d\.ts$/.test(f));
files.sort();

const entries = files.map((f) => {
  const content = readFileSync(join(libDir, f), "utf8");
  // Use JSON.stringify for safe escaping — handles backticks, ${}, backslashes, etc.
  return `  ${JSON.stringify(f)}: ${JSON.stringify(content)},`;
});

const out = `// AUTO-GENERATED by scripts/generate-typecheck-libs.mjs. Do not edit by hand.
// Regenerate via \`npm run build\`. ${files.length} files, ~3 MB raw.
//
// Bundled at build time so the in-isolate \`typecheck\` tool can resolve
// \`/// <reference lib="..." />\` directives without a filesystem.

export const TYPECHECK_LIB_FILES: Record<string, string> = {
${entries.join("\n")}
};
`;

writeFileSync(outFile, out);
console.log(`generate-typecheck-libs: wrote ${files.length} lib files (${out.length.toLocaleString()} bytes) to ${outFile}`);
